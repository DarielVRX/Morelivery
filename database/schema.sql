CREATE EXTENSION IF NOT EXISTS pgcrypto;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'user_role') THEN
    CREATE TYPE user_role AS ENUM ('customer', 'restaurant', 'driver', 'admin');
  END IF;

  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'account_status') THEN
    CREATE TYPE account_status AS ENUM ('active', 'suspended');
  END IF;
END $$;

CREATE TABLE IF NOT EXISTS users (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  full_name VARCHAR(120) NOT NULL,
  alias TEXT,
  email VARCHAR(150) NOT NULL,
  real_email TEXT,
  password_hash TEXT NOT NULL,
  role user_role NOT NULL,
  status account_status NOT NULL DEFAULT 'active',
  address TEXT,
  lat NUMERIC(9,6),
  lng NUMERIC(9,6),
  calle TEXT,
  numero TEXT,
  postal_code TEXT,
  colonia TEXT,
  estado TEXT,
  ciudad TEXT,
  home_lat DOUBLE PRECISION,
  home_lng DOUBLE PRECISION,
  email_verified BOOLEAN NOT NULL DEFAULT false,
  email_verify_token TEXT,
  email_verify_expires TIMESTAMPTZ,
  google_id TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS restaurants (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  owner_user_id UUID NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  name VARCHAR(140) NOT NULL,
  category VARCHAR(80) NOT NULL,
  address TEXT,
  is_open BOOLEAN NOT NULL DEFAULT true,
  is_active BOOLEAN NOT NULL DEFAULT true,
  manual_open_override BOOLEAN DEFAULT NULL,
  profile_photo TEXT,
  lat NUMERIC(9,6),
  lng NUMERIC(9,6),
  postal_code TEXT,
  colonia TEXT,
  estado TEXT,
  ciudad TEXT,
  home_lat DOUBLE PRECISION,
  home_lng DOUBLE PRECISION,
  rating_avg NUMERIC(3,2) DEFAULT NULL,
  rating_count INT NOT NULL DEFAULT 0,
  prep_time_estimate_s INTEGER NOT NULL DEFAULT 600,
  last_prep_time_s INTEGER,
  prep_estimate_updated_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS menu_items (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
