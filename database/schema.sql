  driver_rates_restaurant SMALLINT CHECK (driver_rates_restaurant BETWEEN 1 AND 5),
  driver_comment TEXT,
  restaurant_comment TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
