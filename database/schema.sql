CREATE TABLE IF NOT EXISTS orders (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  customer_id UUID NOT NULL REFERENCES users(id),
  restaurant_id UUID NOT NULL REFERENCES restaurants(id),
  driver_id UUID REFERENCES users(id),
  last_driver_id UUID REFERENCES users(id) ON DELETE SET NULL,
  disputed_by UUID REFERENCES users(id) ON DELETE SET NULL,
  status VARCHAR(30) NOT NULL,
  total_cents INT NOT NULL,
  service_fee_cents INTEGER NOT NULL DEFAULT 0,
  delivery_fee_cents INTEGER NOT NULL DEFAULT 0,
  restaurant_fee_cents INTEGER NOT NULL DEFAULT 0,
  tip_cents INTEGER NOT NULL DEFAULT 0,
  delivered_tip_cents INTEGER NOT NULL DEFAULT 0,
  payment_method TEXT NOT NULL DEFAULT 'cash' CHECK (payment_method IN ('cash', 'card', 'spei')),
  delivery_address TEXT NOT NULL,
  delivery_lat DOUBLE PRECISION,
  delivery_lng DOUBLE PRECISION,
  restaurant_lat NUMERIC(9,6),
  restaurant_lng NUMERIC(9,6),
  estimated_volume_liters NUMERIC(8,3) NOT NULL DEFAULT 0,
  suggestion_text TEXT,
  suggestion_status VARCHAR(20) NOT NULL DEFAULT 'none',
  driver_note TEXT,
  restaurant_note TEXT,
  accepted_at TIMESTAMPTZ,
  preparing_at TIMESTAMPTZ,
  ready_at TIMESTAMPTZ,
  picked_up_at TIMESTAMPTZ,
  delivered_at TIMESTAMPTZ,
  cancelled_at TIMESTAMPTZ,
  offer_cooldown_triggered BOOLEAN NOT NULL DEFAULT false,
  reconnect_deadline TIMESTAMPTZ,
  prep_started_at TIMESTAMPTZ,
  kitchen_estimated_ready TIMESTAMPTZ,
  pickup_wait_s INTEGER NOT NULL DEFAULT 0,
  last_transferred_at TIMESTAMPTZ,
  assignment_score DOUBLE PRECISION,
  route_distance_km DOUBLE PRECISION,
  is_disputed BOOLEAN NOT NULL DEFAULT false,
  disputed_until TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS order_items (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  order_id UUID NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
  menu_item_id UUID REFERENCES menu_items(id) ON DELETE SET NULL,
  quantity INT NOT NULL,
  unit_price_cents INT NOT NULL
